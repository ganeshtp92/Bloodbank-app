    {
        Relatehosp tmp;
        int min;
        for(int i=0;i<a.length-1;i++)
        {
             min = i;

            for(int j=i+1;j<a.length;j++)
            {
                if(a[j].diff<a[min].diff)
                    min = j;

            }
             if(min!=i)
             {
                 tmp = a[i];
                 a[i]=a[min];
                 a[min]=tmp;

             }
        }
        return a;
    }

